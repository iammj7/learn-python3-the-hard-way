# \t is used for tab space  first line is in tab
tabby_cat = "\tI'm tabbed in."
#  dividing string in to two like
persian_cat = "I'm split\non a line."
# single \ is used for escaping ' or " in a string ex: 'iam 6\'2" tall.'
backslash_cat = "I'm \\ a \\ cat."

fat_cat = """
I'll do a list:
\t* Cat food
\t* Fishies
\t* Fishies
\t* Catnip\n\t* Grass
"""

print(tabby_cat)
print(persian_cat)
print(backslash_cat)
print(fat_cat)
